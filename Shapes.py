import time
import turtle

turtle.bgcolor("black")
turtle.pensize(3)
turtle.speed(1)
turtle.color("red")


def m():
    turtle.pendown()
    turtle.forward(150)
    turtle.right(135)
    turtle.forward(50)
    turtle.left(90)
    turtle.forward(50)
    turtle.right(135)
    turtle.forward(150)


turtle.left(90)
m()
turtle.left(90)
turtle.penup()
turtle.forward(50)
turtle.left(90)
m()
